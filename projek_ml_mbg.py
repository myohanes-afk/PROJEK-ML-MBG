import optuna
from sklearn.model_selection import cross_val_score, StratifiedKFold
from sklearn.svm import SVC
from sklearn.pipeline import Pipeline
from sklearn.feature_extraction.text import TfidfVectorizer

optuna.logging.set_verbosity(optuna.logging.WARNING)  # sembunyikan log berlebih

skf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

def objective(trial):
    """Fungsi objektif Optuna untuk SVM multiclass sentimen"""

    # ── Suggest parameter TF-IDF ──────────────────────────────────────────────
    max_features  = trial.suggest_categorical('tfidf__max_features', [3000, 5000, 8000])
    ngram_max     = trial.suggest_int('tfidf__ngram_max', 1, 3)   # (1, ngram_max)
    min_df        = trial.suggest_int('tfidf__min_df', 1, 3)

    # ── Suggest parameter SVM ─────────────────────────────────────────────────
    kernel  = trial.suggest_categorical('clf__kernel', ['linear', 'rbf', 'sigmoid'])
    C       = trial.suggest_float('clf__C', 0.01, 50, log=True)
    gamma   = trial.suggest_categorical('clf__gamma', ['scale', 'auto'])

    pipeline = Pipeline([
        ('tfidf', TfidfVectorizer(
            max_features=max_features,
            ngram_range=(1, ngram_max),
            min_df=min_df,
            sublinear_tf=True
        )),
        ('clf', SVC(
            kernel=kernel,
            C=C,
            gamma=gamma,
            class_weight='balanced',
            random_state=42
        ))
    ])

    scores = cross_val_score(
        pipeline, X, y,
        cv=skf,
        scoring='f1_weighted',
        n_jobs=-1
    )
    return scores.mean()

# ── Jalankan Optuna ───────────────────────────────────────────────────────────
study = optuna.create_study(direction='maximize')

print('⏳ Menjalankan Optuna (50 trials × 5-fold)... bisa 5–15 menit')
