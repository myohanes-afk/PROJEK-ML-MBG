def hapus_emoji(teks):
    """Hapus semua emoji dan karakter unicode non-ASCII"""
    # Hapus emoji menggunakan regex unicode range
    emoji_pattern = re.compile(
        "["
        u"\U0001F600-\U0001F64F"  # emoticons
        u"\U0001F300-\U0001F5FF"  # symbols & pictographs
        u"\U0001F680-\U0001F6FF"  # transport & map
        u"\U0001F1E0-\U0001F1FF"  # flags
        u"\U00002702-\U000027B0"
        u"\U000024C2-\U0001F251"
        u"\U0001f926-\U0001f937"
        u"\U00010000-\U0010ffff"
        u"\u2640-\u2642"
        u"\u2600-\u2B55"
        u"\u200d"
        u"\u23cf"
        u"\u23e9"
        u"\u231a"
        u"\ufe0f"
        u"\u3030"
        "]+",
        flags=re.UNICODE
    )
    return emoji_pattern.sub('', teks)

def cleaning_teks(teks):
    """Pipeline cleaning teks lengkap"""
    if pd.isna(teks):
        return ''

    teks = str(teks)

    # 1. Hapus emoji
    teks = hapus_emoji(teks)

    # 2. Hapus URL / link
    teks = re.sub(r'http\S+|www\.\S+|https\S+', '', teks)

    # 3. Hapus mention (@username)
    teks = re.sub(r'@\w+', '', teks)

    # 4. Hapus hashtag (#topic) — hanya simbol #, teksnya dipertahankan
    teks = re.sub(r'#(\w+)', r'\1', teks)

    # 5. Hapus karakter HTML entities
    teks = re.sub(r'&amp;|&lt;|&gt;|&quot;|&#39;', ' ', teks)

    # 6. Hapus angka
    teks = re.sub(r'\d+', '', teks)

    # 7. Hapus tanda baca & karakter khusus
    teks = re.sub(r'[^\w\s]', ' ', teks)

    # 8. Lowercase
    teks = teks.lower()

    # 9. Normalisasi spasi
    teks = re.sub(r'\s+', ' ', teks).strip()

    # 10. Normalisasi huruf berulang (contoh: 'bagusssss' → 'bagus')
    teks = re.sub(r'(.)\1{2,}', r'\1', teks)

    # 11. Normalisasi slang
    teks = normalisasi_slang(teks)

    return teks

